package org.example;

public class Calc {

    public int summation(int firstTerm, int secondTerm ){
        int resOfSummation;
        resOfSummation = firstTerm + secondTerm;
        return resOfSummation;
    }
    public int subtraction(int firstTerm, int secondTerm){
        int resOfSubtraction;
        resOfSubtraction = firstTerm - secondTerm;
        return resOfSubtraction;
    }

}
